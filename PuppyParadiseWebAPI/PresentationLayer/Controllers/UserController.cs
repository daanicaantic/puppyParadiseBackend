using BusinessLogicLayer.Constants.ExceptionsConstants;
using BusinessLogicLayer.Helpers;
using BusinessLogicLayer.Services.Implementations;
using BusinessLogicLayer.Services.Interfaces;
using DataAccessLayer.Context;
using DataAccessLayer.Repositories.Interfaces;
using DomainLayer.DTOs.UserDTOs;
using DomainLayer.Models;
using Microsoft.AspNetCore.Http;
using Microsoft.AspNetCore.Mvc;

namespace PresentationLayer.Controllers
{ 
    [Route("api/[controller]")]
    [ApiController]
    public class UserController : ControllerBase
    {
        private readonly IUserService _userService;

        public UserController(IUserService userService)
        {
            _userService = userService;
        }

        [Route("GetUser/{id}")]
        [HttpGet]
        public async Task<IActionResult> GetUser(int id)
        {
            try
            {
                var user = await _userService.GetUserById(id);
                return Ok(user);
            }
            catch (Exception ex)
            {
                return BadRequest(ex.Message);
            }
        }

        [Route("GetUserByEmail/{email}")]
        [HttpGet]
        public async Task<IActionResult> GetUserByEmail(string email)
        {
            try
            {
                var user = await _userService.GetUserByEmail(email);
                return Ok(user);
            }
            catch (Exception ex)
            {
                return BadRequest(ex.Message);
            }
        }

        [Route("GetUserByPhoneNumber/{phoneNumber}")]
        [HttpGet]
        public async Task<IActionResult> GetUserByPhoneNumber(string phoneNumber)
        {
            try
            {
                var user = await _userService.GetUserByPhoneNumber(phoneNumber);
                return Ok(user);
            }
            catch (Exception ex)
            {
                return BadRequest(ex.Message);
            }
        }

        [Route("DeleteUser/{id}")]
        [HttpDelete]
        public async Task<IActionResult> DeleteUser(int id)
        {
            try
            {
                await _userService.DeleteUser(id);
                return Ok();
            }
            catch (Exception ex)
            {
                return BadRequest(ex.Message);
            }
        }

        [Route("GetUsersPerPage")]
        [HttpGet]
        public async Task<IActionResult> GetUsersPerPage([FromQuery] UserFilterDTO usersFilter)
        {
            try
            {
                var result = await _userService.GetUsersPerPage(usersFilter);
                return Ok(result);
            }
            catch (Exception ex)
            {
                return BadRequest(ex.Message);
            }
        }

        [Route("UpdateUserInfo")]
        [HttpPut]
        public async Task<IActionResult> UpdateUserInfo([FromBody] UpdateUserInfoDTO updateUserInfoDTO)
        {
            try
            {
                if (!RegexHelper.IsValidEmail(updateUserInfoDTO.Email))
                    throw new Exception(UserExceptionsConstants.InvalidEmailFormat);

                await _userService.UpdateUserInfo(updateUserInfoDTO);
                return Ok();
            }
            catch (Exception ex)
            {
                return BadRequest(ex.Message);
            }
        }

        [Route("UpdateUserPassword")]
        [HttpPut]
        public async Task<IActionResult> UpdateUserPassword([FromBody] UpdateUserPasswordDTO updateUserPasswordDTO)
        {
            try
            {
                if (updateUserPasswordDTO.NewPassword != updateUserPasswordDTO.ConfirmPassword)
                    throw new ArgumentException(UserExceptionsConstants.PasswordsDoNotMatch);

                if (!RegexHelper.IsValidPassword(updateUserPasswordDTO.NewPassword))
                    throw new ArgumentException(UserExceptionsConstants.InvalidPasswordFormat);

                await _userService.UpdateUserPassword(updateUserPasswordDTO);
                return Ok();
            }
            catch (Exception ex)
            {
                return BadRequest(ex.Message);
            }
        }
    }
}
